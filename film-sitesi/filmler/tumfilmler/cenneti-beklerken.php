                <h4 class="post-info-title">Cenneti Beklerken</h4>
                <div class="post-info-meta bg-gray-100">
                  <ul class="post-info-meta-list">
                    <li><span class="icon mdi mdi-calendar-blank"></span><span class="small-text font-weight-sbold">2006-12-15</span></li>
