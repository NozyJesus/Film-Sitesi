                <p>On yedinci yüzyılda, Osmanlı İmparatorluğu'nun başkenti İstanbul'da yaşayan minyatür ustası Eflatun (Serhat Tutumluer), ustalarının kendisine öğrettiklerine karşı gelerek eşinin ve oğlunun ölümü sonrasında, Batılı tarzda portrelerini çizer. Bu tarz portrelerin İslam dinine aykırı olduğunu düşündüğü için içinde çelişkili duygular kabaran Eflatun, bir gün zorla bir Osmanlı vezirinin konağına götürülür. Vezir, Eflatun'dan, Osmanlı İmparatorluğu'na karşı ayaklanan Danyal (Nihat İleri) adlı sözde bir şehzadenin, idamından önce Batılı tarzda portresini yapmasını ister.

Çırağı rehin alınan Eflatun, bu emir karşısında vezirin bir grup silahlı adamıyla birlikte Anadolu bozkırlarına doğru tehlikeli bir yolculuğa çıkar. Yolda rastladığı köle kızı Leyla'yı (Melisa Sözen) acıyarak yanına alan Eflatun, taht kavgasının ortasında bir yandan var olma mücadelesi verirken diğer yandan da bu güzel köle kız ile gizliden gizliye bir aşk yaşamaya başlar. Verdikleri kayıplara rağmen Şehzade Danyal'ın hapsedildiği kaleye vardıklarında grubu büyük bir sürpriz beklemektedir.

Kavganın ve aşkın imparatorluğunda hayatta kalma savaşı veren Eflatun ve Leyla, hayalle gerçeğin harmanlandığı bu büyülü masalda kendi cennetlerini yaratırlar.</p>
